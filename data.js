// 拆分预期目标为独立题目（按分号或句号拆分）
function splitObjective(raw) {
    // 按照中文分号、句号分割，过滤空字符串
    let parts = raw.split(/[；]/).map(s => s.trim()).filter(s => s.length > 0);
    // 如果拆分后为空，则保留原字符串
    if (parts.length === 0) parts = [raw];
    return parts;
}

// 构建完整的题目列表：预期目标、绩效要点、执行效果检查
function buildAllQuizQuestions() {
    const objectiveQuestions = [];
    const keypointQuestions = [];
    const checkQuestions = [];

    rawPerformanceDomains.forEach(domain => {
        // 预期目标拆分
        const objectives = splitObjective(domain.objectiveRaw);
        objectives.forEach(objText => {
            objectiveQuestions.push({
                text: objText,
                correct: domain.name,
                type: "预期目标"
            });
        });
        // 绩效要点（每个已经是独立条目）
        domain.keyPoints.forEach(kp => {
            keypointQuestions.push({
                text: kp,
                correct: domain.name,
                type: "绩效要点"
            });
        });
        // 执行效果检查
        domain.checkPoints.forEach(cp => {
            checkQuestions.push({
                text: cp,
                correct: domain.name,
                type: "执行效果检查"
            });
        });
    });

    return { objectiveQuestions, keypointQuestions, checkQuestions };
}

